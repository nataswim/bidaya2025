        // ğŸ‡¬ğŸ‡§ Admins/editors always see content / ğŸ‡«ğŸ‡· Admins/Ã©diteurs voient toujours le contenu
        if ($user && ($user->hasRole('admin') || $user->hasRole('editor'))) {
