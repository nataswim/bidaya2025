        // ğŸ‡¬ğŸ‡§ Check visibility / ğŸ‡«ğŸ‡· VÃ©rifier la visibilitÃ©
        if ($this->visibility === 'public') {
            return true;
        }
        
        if ($this->visibility === 'authenticated') {
            return $user !== null;
        }
        
        return false;
    }

